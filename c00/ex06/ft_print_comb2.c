#include <unistd.h>

void	ft_print_comb2(void)
{
	char	d;
	int		i;
	int		j;

	i = 0;
	while (i < 99)
	{
		j = i + 1;
		while (j < 100)
		{
			d = i / 10 + 48;
			write(1, &d, 1);
			d = i % 10 + 48;
			write(1, &d, 1);
			write(1, " ", 1);
			d = j / 10 + 48;
			write(1, &d, 1);
			d = j % 10 + 48;
			write(1, &d, 1);
			if (i != 98)
				write(1, ", ", 2);
			j++;
		}
		i++;
	}
}
